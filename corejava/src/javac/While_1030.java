package javac;

public class While_1030 {

	public static void main(String[] args) {
		
	 
		
	
		        int dollars = 3;
		        int days=1;
		        do {
		            System.out.println(" dollar amount i have now : " + dollars);
		            dollars=3+dollars;
		            days++;
		            
		        } while (dollars < 150);
		        
		        System.out.println(" dollar amount i have finally now : " + dollars);
		        
		        System.out.println(" it took me: " + days+ " days ");
		    }
	
		}